def run_pipeline():
    train_df, val_df, test_df, unique_labels = load_data(FILE_PATH)
    tokenizer = AutoTokenizer.from_pretrained(MODEL_NAME)
    model = BiEncoderInfoNCE(MODEL_NAME).to(device)

    train_loader = DataLoader(LegalInfoNCEDataset(train_df, tokenizer), batch_size=BATCH_SIZE, shuffle=True, num_workers=2)

    optimizer = torch.optim.AdamW(model.parameters(), lr=LR)
    scheduler = get_cosine_schedule_with_warmup(optimizer, num_warmup_steps=150, num_training_steps=len(train_loader) * EPOCHS)
    loss_fn = torch.nn.CrossEntropyLoss()

    best_loss = float('inf')
    os.makedirs(SAVE_PATH, exist_ok=True)

    for epoch in range(EPOCHS):
        model.train()
        total_loss = 0
        loop = tqdm(train_loader, desc=f"Epoch {epoch+1}/{EPOCHS}")
        for batch in loop:
            optimizer.zero_grad()

            scores = model(
                batch['input_ids_a'].to(device), batch['attention_mask_a'].to(device),
                batch['input_ids_b'].to(device), batch['attention_mask_b'].to(device)
            )

            labels = torch.arange(scores.size(0)).long().to(device)
            loss = loss_fn(scores, labels)

            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()
            scheduler.step()

            total_loss += loss.item()
            loop.set_postfix(loss=f"{loss.item():.4f}")

        avg = total_loss / len(train_loader)
        print(f"📈 Epoch {epoch+1}: Loss={avg:.4f}")

        if avg < best_loss:
            best_loss = avg
            torch.save(model.state_dict(), f"{SAVE_PATH}/best_model.pt")
            print("🏆 تم حفظ الموديل (أداء أفضل)")

    # الاختبار النهائي
    model.load_state_dict(torch.load(f"{SAVE_PATH}/best_model.pt"))
    target_embs, target_texts = build_target_index(model, tokenizer, unique_labels, device)
    evaluate_retrieval(model, tokenizer, test_df, target_embs, target_texts, device)

run_pipeline()



