# 3. الموديل الرياضي الجديد (InfoNCE)
# ============================================================
class BiEncoderInfoNCE(torch.nn.Module):
    def __init__(self, model_name):
        super().__init__()
        self.encoder = AutoModel.from_pretrained(model_name)
        # Scale factor (Temperature) لزيادة حدة الفروق بين الجمل
        self.scale = 20.0

    def mean_pool(self, token_embeddings, attention_mask):
        mask = attention_mask.unsqueeze(-1).float()
        return (token_embeddings * mask).sum(1) / torch.clamp(mask.sum(1), min=1e-9)

    def encode(self, input_ids, attention_mask):
        out = self.encoder(input_ids=input_ids, attention_mask=attention_mask)
        emb = self.mean_pool(out.last_hidden_state, attention_mask)
        # تطبيع رياضي (L2 Normalization) ضروري جداً هنا
        return torch.nn.functional.normalize(emb, p=2, dim=1)

    def forward(self, input_ids_a, attention_mask_a, input_ids_b, attention_mask_b):
        emb_a = self.encode(input_ids_a, attention_mask_a) # (batch, hidden)
        emb_b = self.encode(input_ids_b, attention_mask_b) # (batch, hidden)

        # ضرب المصفوفات لتوليد مصفوفة التشابه (كل جملة مع كل الجمل الأخرى)
        scores = torch.matmul(emb_a, emb_b.T) * self.scale
        return scores

