def build_sector_based_index(model, tokenizer, unique_labels, device):
    model.eval()
    sector_index = {}

    sector_to_targets = {}
    for label in unique_labels:
        sector, target = label.split(" || ")
        if sector not in sector_to_targets:
            sector_to_targets[sector] = []
        if target not in sector_to_targets[sector]:
            sector_to_targets[sector].append(target)

    print(f"\n🔨 بناء الفهارس الدلالية مفلترة حسب القطاعات (عدد القطاعات: {len(sector_to_targets)})...")

    for sector, targets in sector_to_targets.items():
        embeddings = []
        for i in range(0, len(targets), 32):
            batch = targets[i:i+32]
            enc = tokenizer(batch, max_length=64, padding=True, truncation=True, return_tensors='pt').to(device)
            with torch.no_grad():
                emb = model.encode(enc['input_ids'], enc['attention_mask'])
            embeddings.append(emb.cpu().numpy())

        sector_index[sector] = {
            'embeddings': np.vstack(embeddings),
            'texts': targets
        }

    return sector_index

def predict_sentence_filtered(model, tokenizer, input_text, sector, sector_index, device, top_k=3):
    model.eval()

    if sector not in sector_index:
        return [("قطاع غير معروف", 0.0)]

    target_embeddings = sector_index[sector]['embeddings']
    target_texts = sector_index[sector]['texts']

    query = sector + " " + input_text
    enc = tokenizer(query, max_length=256, truncation=True, return_tensors='pt').to(device)

    with torch.no_grad():
        emb = model.encode(enc['input_ids'], enc['attention_mask']).cpu().numpy()

    sims = cosine_similarity(emb, target_embeddings)[0]

    actual_top_k = min(top_k, len(target_texts))
    top_idx = sims.argsort()[::-1][:actual_top_k]

    return [(target_texts[i], sims[i]) for i in top_idx]

