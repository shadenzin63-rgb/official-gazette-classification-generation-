def build_target_index(model, tokenizer, unique_labels, device):
    model.eval()
    target_embeddings, target_texts = [], []
    unique_targets = list(set([l.split(" || ")[1] for l in unique_labels]))

    print(f"\n🔨 بناء الفهرس الدلالي لـ {len(unique_targets)} جملة هدف...")
    for i in range(0, len(unique_targets), 32):
        batch = unique_targets[i:i+32]
        enc = tokenizer(batch, max_length=64, padding=True, truncation=True, return_tensors='pt').to(device)
        with torch.no_grad():
            emb = model.encode(enc['input_ids'], enc['attention_mask'])
        target_embeddings.append(emb.cpu().numpy())
        target_texts.extend(batch)

    return np.vstack(target_embeddings), target_texts

def predict_sentence(model, tokenizer, input_text, sector, target_embeddings, target_texts, device, top_k=3):
    model.eval()
    query = sector + " " + input_text
    enc = tokenizer(query, max_length=256, truncation=True, return_tensors='pt').to(device)
    with torch.no_grad():
        emb = model.encode(enc['input_ids'], enc['attention_mask']).cpu().numpy()

    sims = cosine_similarity(emb, target_embeddings)[0]
    top_idx = sims.argsort()[::-1][:top_k]
    return [(target_texts[i], sims[i]) for i in top_idx]

def evaluate_retrieval(model, tokenizer, test_df, target_embeddings, target_texts, device):
    correct_1, correct_3, total = 0, 0, len(test_df)

    print("\n🔍 بدء الاختبار النهائي الشامل...")
    for _, row in tqdm(test_df.iterrows(), total=total):
        preds = predict_sentence(model, tokenizer, row['input_text'], row['sector'], target_embeddings, target_texts, device, top_k=3)
        pred_texts = [p[0] for p in preds]
        ref = row['target_text'].strip()

        if pred_texts[0] == ref: correct_1 += 1
        if ref in pred_texts:    correct_3 += 1

    print(f"\n{'='*50}")
    print(f"🎯 التطابق التام (الخيار الأول) Exact Match @1:  {correct_1/total*100:.2f}%")
    print(f"🎯 التطابق التام (أول 3 خيارات) Exact Match @3:  {correct_3/total*100:.2f}%")
    print(f"{'='*50}")

