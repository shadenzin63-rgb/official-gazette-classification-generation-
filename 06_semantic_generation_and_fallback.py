class LegalInfoNCEDataset(Dataset):
    def __init__(self, df, tokenizer, max_len=256):
        self.df = df.reset_index(drop=True)
        self.tokenizer = tokenizer
        self.max_len = max_len

    def __len__(self): return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        text_a = row['sector'] + " " + row['input_text']
        text_b = row['target_text']

        enc_a = self.tokenizer(text_a, max_length=self.max_len, padding='max_length', truncation=True, return_tensors='pt')
        enc_b = self.tokenizer(text_b, max_length=64, padding='max_length', truncation=True, return_tensors='pt')

        return {
            'input_ids_a':      enc_a['input_ids'].squeeze(),
            'attention_mask_a': enc_a['attention_mask'].squeeze(),
            'input_ids_b':      enc_b['input_ids'].squeeze(),
            'attention_mask_b': enc_b['attention_mask'].squeeze()
        }

