# 2. دالة حساب مقياس ROUGE المخصصة للغة العربية
def arabic_rouge(refs, preds):
    r1_scores, r2_scores, rL_scores = [], [], []
    for ref, pred in zip(refs, preds):
        ref = ref.strip()
        pred = pred.strip()
        if not ref or not pred:
            r1_scores.append(0); r2_scores.append(0); rL_scores.append(0)
            continue
        ref_tokens, pred_tokens = ref.split(), pred.split()

        # ROUGE-1
        overlap_1 = len(set(ref_tokens) & set(pred_tokens))
        r1_scores.append(overlap_1 / max(len(ref_tokens), 1))

        # ROUGE-2
        ref_bigrams = set([(ref_tokens[i], ref_tokens[i+1]) for i in range(len(ref_tokens)-1)])
        pred_bigrams = set([(pred_tokens[i], pred_tokens[i+1]) for i in range(len(pred_tokens)-1)])
        overlap_2 = len(ref_bigrams & pred_bigrams)
        r2_scores.append(overlap_2 / max(len(ref_tokens)-1, 1))

        # ROUGE-L (أطول سلسلة متطابقة)
        def lcs(x, y):
            m, n = len(x), len(y)
            dp = [[0]*(n+1) for _ in range(m+1)]
            for i in range(1, m+1):
                for j in range(1, n+1):
                    if x[i-1] == y[j-1]: dp[i][j] = dp[i-1][j-1] + 1
                    else: dp[i][j] = max(dp[i-1][j], dp[i][j-1])
            return dp[m][n]

        rL_scores.append(lcs(ref_tokens, pred_tokens) / max(len(ref_tokens), 1))

    return np.mean(r1_scores), np.mean(r2_scores), np.mean(rL_scores)

